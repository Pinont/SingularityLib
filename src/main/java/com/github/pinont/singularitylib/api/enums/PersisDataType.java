package com.github.pinont.singularitylib.api.enums;

/**
 * Enumeration of persistent data types for storing data in ItemStacks.
 * Each type corresponds to a Bukkit PersistentDataType for data storage.
 */
public enum PersisDataType {
    /**
     * String data type.
     */
    STRING,
    /**
     * Byte data type.
     */
    BYTE,
    /**
     * Short data type.
     */
    SHORT,
    /**
     * Integer data type.
     */
    INT,
    /**
     * Long data type.
     */
    LONG,
    /**
     * Float data type.
     */
    FLOAT,
    /**
     * Double data type.
     */
    DOUBLE,
    /**
     * Byte array data type.
     */
    BYTE_ARRAY,
    /**
     * Integer array data type.
     */
    INT_ARRAY,
    /**
     * Long array data type.
     */
    LONG_ARRAY
}