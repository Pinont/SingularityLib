package com.github.pinont.singularitylib.api.enums;

/**
 * Enumeration of player inventory slot types.
 * Represents different equipment and hand slots in a player's inventory.
 */
public enum PlayerInventorySlotType {
    /**
     * Main hand slot.
     */
    MAINHAND,
    /**
     * Off hand slot.
     */
    OFFHAND,
    /**
     * Helmet armor slot.
     */
    ARMOR_HEAD,
    /**
     * Chestplate armor slot.
     */
    ARMOR_CHEST,
    /**
     * Leggings armor slot.
     */
    ARMOR_LEGS,
    /**
     * Boots armor slot.
     */
    ARMOR_FEET,
}
